public enum ThuTrongtuan {
    Thu2,thu3,thu4,thu5;
    private int ma ;
    private int tiengviet;
    private int tienganh;

    ThuTrongtuan() {
    }

    public int getMa() {
        return ma;
    }

    public void setMa(int ma) {
        this.ma = ma;
    }

    public int getTiengviet() {
        return tiengviet;
    }

    public void setTiengviet(int tiengviet) {
        this.tiengviet = tiengviet;
    }

    public int getTienganh() {
        return tienganh;
    }

    public void setTienganh(int tienganh) {
        this.tienganh = tienganh;
    }
}
